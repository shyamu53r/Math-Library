package com.example.mymathlibrary;

import android.content.Context;

public class MathOperations {
    public static int addition(Context c,int a,int b) {
        return a+b;
    }
    public static int multiply(Context c,int a,int b) {
        return a*b;
    }
    public static int subtraction(Context c,int a,int b) {
        return a-b;
    }
    public static int division(Context c,int a,int b) {
        return a/b;
    }
}
